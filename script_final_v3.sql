-- 5) CATÁLOGOS
---------------------

CREATE TABLE ACTIVE_INGREDIENT (
  ACT_INGRE_ID    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  ACT_INGREDIENT  VARCHAR2(200) NOT NULL
);

CREATE TABLE PHARMACEUTICAL_FORM (
  PHARM_FORM_ID  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  PHARMA_FORM    VARCHAR2(100) NOT NULL
);

CREATE TABLE UNIT_MEASURE (
  UNIT_MEA_ID             NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  UNIT_MEASURE_MEDICINE   VARCHAR2(20) NOT NULL
);

CREATE TABLE CATEGORY_MEDICINE (
  CATEGORY_MED_ID  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  CATEGORY         VARCHAR2(255) NOT NULL
);

---------------------
-- 6) MEDICINES 
---------------------

CREATE TABLE MEDICINES (
  MEDICINE_ID     NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  NAME_MEDICATION VARCHAR2(255) NOT NULL,
  STATUS_MED      VARCHAR2(20)  NOT NULL,
  CATEGORY_MED_ID NUMBER NOT NULL,
  UNIT_MEA_ID     NUMBER NOT NULL,
  CONSTRAINT FK_MED_CATEGORY  FOREIGN KEY (CATEGORY_MED_ID) REFERENCES CATEGORY_MEDICINE(CATEGORY_MED_ID),
  CONSTRAINT FK_MED_UNIT      FOREIGN KEY (UNIT_MEA_ID)     REFERENCES UNIT_MEASURE(UNIT_MEA_ID)
);

CREATE TABLE MEDICINE_ACTIVE_INGR (
  MED_ACTIVE_INGR_ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  MEDICINE_ID  NUMBER NOT NULL,
  ACT_INGRE_ID NUMBER NOT NULL,
  CONSTRAINT FK_MAI_MEDICINE  FOREIGN KEY (MEDICINE_ID)  REFERENCES MEDICINES(MEDICINE_ID),
  CONSTRAINT FK_MAI_INGRED    FOREIGN KEY (ACT_INGRE_ID) REFERENCES ACTIVE_INGREDIENT(ACT_INGRE_ID)
);

CREATE TABLE MEDICINE_PHARM_FORM (
  MED_PHARM_FORM_ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  MEDICINE_ID   NUMBER NOT NULL,
  PHARM_FORM_ID NUMBER NOT NULL,
  CONSTRAINT FK_MPF_MEDICINE  FOREIGN KEY (MEDICINE_ID)   REFERENCES MEDICINES(MEDICINE_ID),
  CONSTRAINT FK_MPF_PHARM     FOREIGN KEY (PHARM_FORM_ID) REFERENCES PHARMACEUTICAL_FORM(PHARM_FORM_ID)
);

------------------------------------------
-- 7) LOTES / ESTOQUE / MOVIMENTOS
------------------------------------------

CREATE TABLE BATCH_MEDICINE (
  BATCH_ID  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  BATCH_NUMBER       VARCHAR2(255) NOT NULL,
  CURRENT_QUANTITY   NUMBER        NOT NULL,
  MANUFACTURING_DATE DATE          NOT NULL,
  EXPIRATION_DATE    DATE          NOT NULL,
  MANUFAC_ID         NUMBER        NOT NULL,
  CONSTRAINT FK_BATCH_MANUFACTURER
    FOREIGN KEY (MANUFAC_ID) REFERENCES MANUFACTURER(MANUFAC_ID)
);

CREATE TABLE MOVEMENT_TYPE (
  MOVEMENT_TYPE_ID  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  TYPE_NAME         VARCHAR2(30) NOT NULL
);

CREATE TABLE STOCK (
  STOCK_ID           NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  QUANTITY           NUMBER(6) NOT NULL,
  BATCH_ID  NUMBER    NOT NULL,
  MEDICINE_ID        NUMBER    NOT NULL,
  LOCATION_ID_STOCK  NUMBER    NOT NULL,
  CONSTRAINT FK_STOCK_BATCH    FOREIGN KEY (BATCH_ID) REFERENCES BATCH_MEDICINE(BATCH_ID),
  CONSTRAINT FK_STOCK_MED      FOREIGN KEY (MEDICINE_ID)       REFERENCES MEDICINES(MEDICINE_ID),
  CONSTRAINT FK_STOCK_LOCATION FOREIGN KEY (LOCATION_ID_STOCK) REFERENCES LOCATION_STOCK(LOCATION_ID_STOCK)
);

---------------------
-- 8) DISPENSAÇÃO
---------------------

CREATE TABLE MEDICINE_DISPENSE (
  DISPENSATION_ID     NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  DATE_DISPENSATION   DATE          NOT NULL,
  QUANTITY_DISPENSED  NUMBER(6)     NOT NULL,
  DESTINATION         VARCHAR2(255) NOT NULL,
  OBSERVATION         VARCHAR2(255),
  USER_ID             NUMBER        NOT NULL,
  CONSTRAINT FK_DISP_USER       FOREIGN KEY (USER_ID)          REFERENCES USERS_SYS(USER_ID)
);

------------------------------------------------
--  9) NOVA TABELA: STOCK_MOVEMENT 
------------------------------------------------
CREATE TABLE STOCK_MOVEMENT (
  STOCK_MOVEMENT_ID   NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  QUANTITY_DISPENSED  NUMBER(6)    NOT NULL, 
  DATA_MOVIMENT DATE NOT NULL,
  MOVEMENT_TYPE_ID    NUMBER,  
  STOCK_ID            NUMBER       NOT NULL,  
  DISPENSATION_ID     NUMBER       NOT NULL, 
  USER_ID             NUMBER,                 
  CONSTRAINT FK_STKMOV_MOVTYPE   FOREIGN KEY (MOVEMENT_TYPE_ID) REFERENCES MOVEMENT_TYPE(MOVEMENT_TYPE_ID),
  CONSTRAINT FK_STKMOV_STOCK     FOREIGN KEY (STOCK_ID)         REFERENCES STOCK(STOCK_ID),
  CONSTRAINT FK_STKMOV_DISPENSE  FOREIGN KEY (DISPENSATION_ID)  REFERENCES MEDICINE_DISPENSE(DISPENSATION_ID),
  CONSTRAINT FK_STKMOV_USER      FOREIGN KEY (USER_ID)          REFERENCES USERS_SYS(USER_ID)

);


